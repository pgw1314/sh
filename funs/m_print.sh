#!/bin/bash
#################################################
#
#该脚本为打印模块，在命令行打印不同颜色的字体
#
#################################################



#-------------------------------------------------
#函数名称： 打印红色
#
#参数：字符串
#   
#功能：在命令行中打印红色字体
#
#-------------------------------------------------
print_r(){
    echo -e "\033[31m $@ \033[0m"
}



#-------------------------------------------------
#函数名称： 打印绿色
#
#参数：字符串
#   
#功能：在命令行中打印绿色字体
#
#-------------------------------------------------
print_g(){
    echo -e "\033[32m $@ \033[0m"
}



#-------------------------------------------------
#函数名称： 打印蓝色
#
#参数：字符串
#   
#功能：在命令行中打印蓝色字体
#
#-------------------------------------------------
print_b(){
    echo -e "\033[34m $@ \033[0m"
}




#-------------------------------------------------
#函数名称： 打印黄色
#
#参数：字符串
#   
#功能：在命令行中打印黄色字体
#
#-------------------------------------------------
print_y(){
    echo -e "\033[33m $@ \033[0m"
}